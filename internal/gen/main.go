// Command gen writes the cross-domain locale umbrella packages under
// gocldr/locales. For every locale tag that exists under both
// datetime/locales/<tag> and number/locales/<tag>, it emits
// locales/<tag>/<sanitized>_gen.go (package locale) that blank-imports the
// datetime and number data packages for that tag, plus locales/all/all_gen.go
// that pulls in both domains' "all" aggregators.
//
// It is invoked via the //go:generate directive in generate.go:
//
//	go generate ./...   # (the root directive) — or directly: go run ./internal/gen
//
// This generator reads NO CLDR data; it only enumerates the existing per-locale
// directories and writes import stubs, so it is not CLDR-version-sensitive and
// is safe to run on the host.
package main

import (
	"bytes"
	"fmt"
	"log"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/hakastein/gocldr/internal/cldr"
)

func main() {
	log.SetFlags(0)

	// Resolve the module root relative to this generator. The //go:generate
	// directive (and `go run ./internal/gen`) runs with the module root as the
	// working directory, so paths are relative to it.
	datetimeLocales := filepath.Join("datetime", "locales")
	numberLocales := filepath.Join("number", "locales")
	umbrellaLocales := "locales"

	tags := enumerateTags(datetimeLocales)
	// Sanity check: the number domain must offer the same per-locale set.
	if numberTags := enumerateTags(numberLocales); !equalStrings(tags, numberTags) {
		log.Fatalf("datetime and number locale sets differ: datetime=%d number=%d", len(tags), len(numberTags))
	}

	for _, tag := range tags {
		var b bytes.Buffer
		b.WriteString("// Code generated by internal/gen. DO NOT EDIT.\n\n")
		b.WriteString("// Package locale registers both datetime and number CLDR data for one locale.\n")
		b.WriteString("package locale\n\n")
		b.WriteString("import (\n")
		b.WriteString(fmt.Sprintf("\t_ \"github.com/hakastein/gocldr/datetime/locales/%s\"\n", tag))
		b.WriteString(fmt.Sprintf("\t_ \"github.com/hakastein/gocldr/number/locales/%s\"\n", tag))
		b.WriteString(")\n")

		dir := filepath.Join(umbrellaLocales, tag)
		if err := os.MkdirAll(dir, 0o755); err != nil {
			log.Fatal(err)
		}
		if err := cldr.WriteFormatted(filepath.Join(dir, sanitize(tag)+"_gen.go"), b.Bytes()); err != nil {
			log.Fatal(err)
		}
	}

	// locales/all aggregates both domains' "all" packages.
	var ab bytes.Buffer
	ab.WriteString("// Code generated by internal/gen. DO NOT EDIT.\n\n")
	ab.WriteString("// Package all registers all CLDR locale data for both datetime and number.\n")
	ab.WriteString("package all\n\n")
	ab.WriteString("import (\n")
	ab.WriteString("\t_ \"github.com/hakastein/gocldr/datetime/locales/all\"\n")
	ab.WriteString("\t_ \"github.com/hakastein/gocldr/number/locales/all\"\n")
	ab.WriteString(")\n")
	allDir := filepath.Join(umbrellaLocales, "all")
	if err := os.MkdirAll(allDir, 0o755); err != nil {
		log.Fatal(err)
	}
	if err := cldr.WriteFormatted(filepath.Join(allDir, "all_gen.go"), ab.Bytes()); err != nil {
		log.Fatal(err)
	}

	log.Printf("wrote %d locales/<tag>/<tag>_gen.go + locales/all", len(tags))
}

// enumerateTags returns the sorted directory names directly under dir,
// excluding the "all" aggregator.
func enumerateTags(dir string) []string {
	ents, err := os.ReadDir(dir)
	if err != nil {
		log.Fatal(err)
	}
	tags := make([]string, 0, len(ents))
	for _, e := range ents {
		if !e.IsDir() || e.Name() == "all" {
			continue
		}
		tags = append(tags, e.Name())
	}
	sort.Strings(tags)
	return tags
}

// sanitize turns a canonical locale tag into a filesystem-safe filename stem by
// replacing the BCP 47 subtag separator with an underscore (e.g. en-GB ->
// en_GB). The directory keeps the canonical tag verbatim.
func sanitize(tag string) string {
	return strings.ReplaceAll(tag, "-", "_")
}

func equalStrings(a, b []string) bool {
	if len(a) != len(b) {
		return false
	}
	for i := range a {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}
