// Command gen reads the CLDR cardinal and ordinal plural-rule data
// (cldr-core's plurals.json / ordinals.json, located via $CLDR_DATA — set by
// the pinned gen image; run via `make gen`, never on the host) and emits a
// generated Go source file (tables_gen.go) for package plural.
//
// Each CLDR condition is parsed (UTS #35 grammar) into a native Go boolean
// expression over the operands. Locales that share an identical set of
// conditions are mapped to a single shared rule set to keep the file compact.
package main

import (
	"bytes"
	"encoding/json"
	"flag"
	"fmt"
	"log"
	"path/filepath"
	"sort"
	"strconv"
	"strings"

	"github.com/hakastein/gocldr/internal/cldr"
)

func main() {
	log.SetFlags(0)
	outPath := flag.String("out", "tables_gen.go", "output Go file")
	flag.Parse()

	supplemental := filepath.Join(cldr.MustDataDir(), "cldr-core", "supplemental")
	cardinal := loadRules(filepath.Join(supplemental, "plurals.json"), "plurals-type-cardinal")
	ordinal := loadRules(filepath.Join(supplemental, "ordinals.json"), "plurals-type-ordinal")

	var buf bytes.Buffer
	buf.WriteString("// Code generated by internal/gen; DO NOT EDIT.\n")
	buf.WriteString("// Source: Unicode CLDR plurals.json / ordinals.json.\n\n")
	buf.WriteString("package plural\n\n")

	cardSets := emitTable(&buf, "cardinal", cardinal)
	ordSets := emitTable(&buf, "ordinal", ordinal)

	if err := cldr.WriteFormatted(*outPath, buf.Bytes()); err != nil {
		log.Fatal(err)
	}
	log.Printf("wrote %s: cardinal locales=%d (rule-sets=%d), ordinal locales=%d (rule-sets=%d)",
		*outPath, len(cardinal.order), cardSets, len(ordinal.order), ordSets)
}

// localeRules is the ordered set of (locale -> categoryConditions) parsed from a
// CLDR file.
type localeRules struct {
	order []string                     // locale keys in file order
	conds map[string]map[string]string // locale -> category -> raw condition
	cats  map[string][]string          // locale -> ordered categories present
}

var categoryOrder = []string{"zero", "one", "two", "few", "many", "other"}

func loadRules(path, key string) localeRules {
	var doc struct {
		Supplemental map[string]json.RawMessage `json:"supplemental"`
	}
	cldr.MustJSON(path, &doc)
	raw, ok := doc.Supplemental[key]
	if !ok {
		log.Fatalf("key %q not found in %s", key, path)
	}
	var section map[string]map[string]string
	if err := json.Unmarshal(raw, &section); err != nil {
		log.Fatal(err)
	}
	lr := localeRules{
		conds: make(map[string]map[string]string),
		cats:  make(map[string][]string),
	}
	for loc, rules := range section {
		lr.order = append(lr.order, loc)
		cm := make(map[string]string)
		for ruleKey, raw := range rules {
			cat := strings.TrimPrefix(ruleKey, "pluralRule-count-")
			cond := strings.TrimSpace(strings.SplitN(raw, "@", 2)[0])
			cm[cat] = cond
		}
		lr.conds[loc] = cm
		for _, c := range categoryOrder {
			if _, ok := cm[c]; ok {
				lr.cats[loc] = append(lr.cats[loc], c)
			}
		}
	}
	sort.Strings(lr.order)
	return lr
}

// emitTable writes the rule-set functions and the locale->ruleSet map for one
// plural type. It returns the number of distinct rule sets emitted.
func emitTable(buf *bytes.Buffer, kind string, lr localeRules) int {
	// Build a signature per locale so identical rule sets are shared.
	type setInfo struct {
		id    int
		cats  []string
		exprs []string // Go boolean expression per non-other category
	}
	sigToSet := map[string]*setInfo{}
	var sets []*setInfo
	localeToSig := map[string]string{}

	for _, loc := range lr.order {
		cm := lr.conds[loc]
		cats := lr.cats[loc]
		var nonOther []string
		var exprs []string
		for _, c := range cats {
			if c == "other" {
				continue
			}
			cond := cm[c]
			expr := translate(cond)
			nonOther = append(nonOther, c)
			exprs = append(exprs, expr)
		}
		// Signature ignores trailing "other".
		sig := strings.Join(nonOther, "\x00") + "\x01" + strings.Join(exprs, "\x00")
		localeToSig[loc] = sig
		if _, ok := sigToSet[sig]; !ok {
			si := &setInfo{id: len(sets), cats: nonOther, exprs: exprs}
			sigToSet[sig] = si
			sets = append(sets, si)
		}
	}

	// Emit rule-set variables.
	for _, si := range sets {
		fmt.Fprintf(buf, "var %sSet%d = &ruleSet{\n", kind, si.id)
		// cats slice (including trailing Other)
		buf.WriteString("\tcats: []Category{")
		for _, c := range si.cats {
			buf.WriteString(catConst(c) + ", ")
		}
		buf.WriteString("Other},\n")
		buf.WriteString("\tpreds: []rule{\n")
		for _, e := range si.exprs {
			fmt.Fprintf(buf, "\t\tfunc(o Operands) bool { return %s },\n", e)
		}
		buf.WriteString("\t},\n")
		buf.WriteString("}\n\n")
	}

	// Emit locale -> rule set map.
	fmt.Fprintf(buf, "var %sRules = map[string]*ruleSet{\n", kind)
	for _, loc := range lr.order {
		si := sigToSet[localeToSig[loc]]
		fmt.Fprintf(buf, "\t%q: %sSet%d,\n", loc, kind, si.id)
	}
	buf.WriteString("}\n\n")
	return len(sets)
}

func catConst(c string) string {
	switch c {
	case "zero":
		return "Zero"
	case "one":
		return "One"
	case "two":
		return "Two"
	case "few":
		return "Few"
	case "many":
		return "Many"
	default:
		return "Other"
	}
}

// ---- CLDR condition grammar parser -> Go expression ----
//
// condition     = and_condition ('or' and_condition)*
// and_condition = relation ('and' relation)*
// relation      = expr ('=' | '!=') range_list
// expr          = operand ('%' value)?
// operand       = 'n' | 'i' | 'v' | 'w' | 'f' | 't' | 'c' | 'e'
// range_list    = (range | value) (',' (range | value))*
// range         = value '..' value

// translate converts a CLDR condition string into a Go boolean expression over
// a variable o of type Operands. An empty condition (the "other" catch-all)
// yields "true".
func translate(cond string) string {
	cond = strings.TrimSpace(cond)
	if cond == "" {
		return "true"
	}
	orParts := splitKeyword(cond, "or")
	var ors []string
	for _, op := range orParts {
		ors = append(ors, translateAnd(op))
	}
	if len(ors) == 1 {
		return ors[0]
	}
	return strings.Join(ors, " || ")
}

func translateAnd(s string) string {
	andParts := splitKeyword(s, "and")
	var ands []string
	for _, ap := range andParts {
		ands = append(ands, translateRelation(ap))
	}
	if len(ands) == 1 {
		return ands[0]
	}
	// Parenthesise each AND group so it composes correctly under ||.
	return "(" + strings.Join(ands, " && ") + ")"
}

func translateRelation(s string) string {
	s = strings.TrimSpace(s)
	neg := false
	var lhs, rhs string
	if idx := strings.Index(s, "!="); idx >= 0 {
		neg = true
		lhs = s[:idx]
		rhs = s[idx+2:]
	} else if idx := strings.Index(s, "="); idx >= 0 {
		lhs = s[:idx]
		rhs = s[idx+1:]
	} else {
		log.Fatalf("relation without operator: %q", s)
	}
	operandExpr, isInt := translateExpr(strings.TrimSpace(lhs))
	member := translateRangeList(operandExpr, isInt, strings.TrimSpace(rhs))
	if neg {
		return "!(" + member + ")"
	}
	return member
}

// translateExpr returns a Go expression for the left-hand operand expression
// and whether it yields an integer (int64) value. Modulo (operand % value)
// always yields an integer.
func translateExpr(s string) (string, bool) {
	s = strings.TrimSpace(s)
	if idx := strings.IndexByte(s, '%'); idx >= 0 {
		operand := strings.TrimSpace(s[:idx])
		modStr := strings.TrimSpace(s[idx+1:])
		mod, err := strconv.ParseInt(modStr, 10, 64)
		if err != nil {
			log.Fatalf("bad modulus %q", modStr)
		}
		base, baseInt := operandGo(operand)
		if baseInt {
			return fmt.Sprintf("%s%%%d", base, mod), true
		}
		// n is a float: the modulo keeps any fractional part (UTS #35 modulo is
		// n - m*floor(n/m)), and the subsequent comparison uses float-operand
		// membership rules (so a fractional result never matches an integer
		// value or range). modNf returns the float remainder.
		return fmt.Sprintf("modNf(%s, %d)", base, mod), false
	}
	return operandGo(s)
}

// operandGo maps a CLDR operand letter to its Operands field accessor and
// reports whether that field is an integer type.
func operandGo(op string) (string, bool) {
	switch op {
	case "n":
		return "o.N", false
	case "i":
		return "o.I", true
	case "v":
		return "o.V", true
	case "w":
		return "o.W", true
	case "f":
		return "o.F", true
	case "t":
		return "o.T", true
	case "c", "e":
		return "o.C", true
	default:
		log.Fatalf("unknown operand %q", op)
		return "", false
	}
}

// translateRangeList builds a membership test: whether the operand expression
// (operandExpr) is in the given range/value list. isInt reports whether the
// operand expression is an integer type; if not (operand n), each comparison
// also requires the value to be integer-valued for range membership.
func translateRangeList(operandExpr string, isInt bool, list string) string {
	items := strings.Split(list, ",")
	var terms []string
	for _, it := range items {
		it = strings.TrimSpace(it)
		if it == "" {
			continue
		}
		if dot := strings.Index(it, ".."); dot >= 0 {
			lo := strings.TrimSpace(it[:dot])
			hi := strings.TrimSpace(it[dot+2:])
			terms = append(terms, rangeTerm(operandExpr, isInt, lo, hi))
		} else {
			terms = append(terms, eqTerm(operandExpr, isInt, it))
		}
	}
	if len(terms) == 0 {
		return "false"
	}
	if len(terms) == 1 {
		return terms[0]
	}
	return "(" + strings.Join(terms, " || ") + ")"
}

func eqTerm(operandExpr string, isInt bool, val string) string {
	if isInt {
		return fmt.Sprintf("%s == %s", operandExpr, val)
	}
	// Float operand compared to a single value: direct float equality.
	return fmt.Sprintf("%s == %s", operandExpr, floatLit(val))
}

func rangeTerm(operandExpr string, isInt bool, lo, hi string) string {
	if isInt {
		return fmt.Sprintf("%s >= %s && %s <= %s", operandExpr, lo, operandExpr, hi)
	}
	// Float operand (n) in an integer range a..b: matches only if the value is
	// an integer and within [a,b]. Use the inRangeN helper to enforce that.
	return fmt.Sprintf("inRangeN(%s, %s, %s)", operandExpr, lo, hi)
}

func floatLit(v string) string {
	// Ensure the literal is treated as float64.
	if strings.ContainsAny(v, ".eE") {
		return v
	}
	return v + ".0"
}

// splitKeyword splits s on a whitespace-delimited keyword ("and" / "or").
func splitKeyword(s, kw string) []string {
	tokens := strings.Fields(s)
	var parts []string
	var cur []string
	for _, t := range tokens {
		if t == kw {
			parts = append(parts, strings.Join(cur, " "))
			cur = nil
			continue
		}
		cur = append(cur, t)
	}
	parts = append(parts, strings.Join(cur, " "))
	return parts
}
